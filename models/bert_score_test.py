# 2) 모델 로딩 (학습된 모델 경로)
MODEL_DIR = r"D:\PyCharm\dialect_translator\models\model03"
model = BartForConditionalGeneration.from_pretrained(MODEL_DIR)
tokenizer = AutoTokenizer.from_pretrained(MODEL_DIR)

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
model.to(device)
model.eval()

# 3) 모델 예측(번역) 결과를 저장할 리스트
predictions = []

# 4) 배치 크기 설정
batch_size = 16
num_batches = math.ceil(len(dialects) / batch_size)

print("모델 추론(번역) 진행 중...\n")
for i in tqdm(range(num_batches), desc="Translating"):
    batch_dialects = dialects[i*batch_size : (i+1)*batch_size]

    inputs = tokenizer(
        batch_dialects,
        return_tensors="pt",
        max_length=64,
        truncation=True,
        padding=True
    )
    inputs = {k: v.to(device) for k, v in inputs.items()}

    with torch.no_grad():
        output_ids = model.generate(
            inputs["input_ids"],
            attention_mask=inputs["attention_mask"],
            num_beams=4,
            max_length=64,
            early_stopping=True
        )

    # 디코딩
    batch_preds = tokenizer.batch_decode(output_ids, skip_special_tokens=True)
    predictions.extend(batch_preds)

# 5) BERTScore 계산
print("\nBERTScore 계산 중...")
P, R, F1 = score(predictions, standards, lang="ko")

# 6) 결과 출력
mean_p = P.mean().item()
mean_r = R.mean().item()
mean_f = F1.mean().item()

print("=== BERTScore 결과 (한국어) ===")
print(f"Precision: {mean_p:.4f}")
print(f"Recall   : {mean_r:.4f}")
print(f"F1       : {mean_f:.4f}")
print("\n평가 완료!")
